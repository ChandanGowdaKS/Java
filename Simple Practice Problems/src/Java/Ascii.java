// Printing ASCII values

package Java;

public class Ascii {
    public static void main(String[] args) {
        char a = 'C';
        int b = a;
        System.out.println("The ascii value is : "+ b);
        int c = (int) a;
        System.out.println("The ASCII Value is by Type casting method : "+ c);

    }
}

